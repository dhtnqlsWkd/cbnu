
public class Variable {

	public static void main(String[] args) {
		int a;
		a = 10;
		System.out.println("a = " + a);
		a = 20;
		System.out.println("a = " + a);
		System.out.println("a + a = " + (a + a));
		int b = 30;
		System.out.println("b = " + b);
		float f;
		f = 0.123456789f;
		System.out.println("f = " + f);
		double d;
		d = 0.12345678901234567;
		System.out.println("d = " + d);
	}

}
